@pytest.fixture(autouse=True)
def reset_logging_state():
    # Ensure each test starts from a known state.

    init_logging(force=True, service="test", level="DEBUG", fmt="console")
    set_correlation_id(None)
    yield
    # Clean up for the next test.
    init_logging(force=True, service="test", level="DEBUG", fmt="console")
    set_correlation_id(None)


def test_get_logger_returns_bound_logger() -> None:
    log = get_logger("test.module")
    # structlog bound loggers expose .info / .debug / etc.
    assert hasattr(log, "info")
    assert hasattr(log, "error")


def test_logger_emits_without_raising(capfd: pytest.CaptureFixture[str]) -> None:
    log = get_logger("test")
    log.info("hello", what="world")
    out = capfd.readouterr().out
    assert "hello" in out
    assert "world" in out


def test_service_name_is_stamped(capfd: pytest.CaptureFixture[str]) -> None:
    init_logging(service="my-svc", force=True, fmt="console")
    get_logger().info("ping")
    out = capfd.readouterr().out
    assert "my-svc" in out


def test_bind_context_appears_in_output(capfd: pytest.CaptureFixture[str]) -> None:
    with bind_context(project_id="proj_x", line_id="line_1"):
        get_logger().info("did-something")
    out = capfd.readouterr().out
    assert "proj_x" in out
    assert "line_1" in out


def test_bind_context_is_scoped(capfd: pytest.CaptureFixture[str]) -> None:
    with bind_context(project_id="inside"):
        pass
    # Outside the block, project_id should NOT leak.
    get_logger().info("after")
    out = capfd.readouterr().out
    assert "inside" not in out


def test_nested_bind_context_overrides_then_restores(
    capfd: pytest.CaptureFixture[str],
) -> None:
    with bind_context(project_id="outer"):
        with bind_context(line_id="inner"):
            get_logger().info("nested")
        out_inner = capfd.readouterr().out
    get_logger().info("after")
    out_after = capfd.readouterr().out

    assert "outer" in out_inner
    assert "inner" in out_inner
    # After the outer block exits, outer is gone too.
    assert "outer" not in out_after


def test_correlation_id_autogenerated() -> None:
    set_correlation_id(None)
    cid = get_correlation_id()
    assert cid
    assert isinstance(cid, str)
    # Reading again returns the same id.
    assert get_correlation_id() == cid


def test_correlation_id_is_overridable() -> None:
    set_correlation_id("req-123")
    assert get_correlation_id() == "req-123"


def test_correlation_id_appears_in_logs(
    capfd: pytest.CaptureFixture[str],
) -> None:
    set_correlation_id("trace-abc")
    get_logger().info("event")
    out = capfd.readouterr().out
    assert "trace-abc" in out


