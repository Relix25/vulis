def test_settings_defaults() -> None:
    get_settings.cache_clear()
    s = VulisSettings()
    assert s.surface in ("workstation", "server", "edge")
    assert s.environment in ("dev", "staging", "prod")
    assert s.log_level == "INFO"
    assert s.storage_backend == "local-fs"


def test_settings_env_prefix(monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.setenv("VULIS_SERVICE_NAME", "dataset")
    monkeypatch.setenv("VULIS_LOG_LEVEL", "DEBUG")
    s = VulisSettings()
    assert s.service_name == "dataset"
    assert s.log_level == "DEBUG"


def test_settings_masked_dump_hides_secrets(monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.setenv("VULIS_POSTGRES_DSN", "postgresql://u:secret@h/db")
    s = VulisSettings()
    dump = s.masked_dump()
    assert "secret" not in dump["postgres_dsn"]
    assert dump["postgres_dsn"] == "***"


def test_settings_masked_dump_shows_none() -> None:
    s = VulisSettings()
    dump = s.masked_dump()
    assert dump["postgres_dsn"] == "<none>"


def test_settings_validates_surface(monkeypatch: pytest.MonkeyPatch) -> None:
    monkeypatch.setenv("VULIS_SURFACE", "moon")
    with pytest.raises(ValidationError):
        VulisSettings()


def test_logging_accepts_int_level() -> None:
    init_logging(level=logging.WARNING, force=True)


def test_logging_idempotent_without_force() -> None:
    # Calling again without force should be a no-op (no exception).
    init_logging(service="a", force=True)
    init_logging(service="b")  # ignored
